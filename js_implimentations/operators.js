//Exponentiation Assignment Example
var x = 10;
x **= 5;
console.log("Exponentiation Assignment Example => \n "+x);

//Left Shift Assignment Example
var x = -100;
x <<= 5;
console.log("Left Shift Assignment Example => \n "+x);

//Right Shift Assignment Example
var x = -100;
x >>= 5;
console.log("Right Shift Assignment Example => \n "+x);

//Unsigned Right Shift Assignment Example
var x = -100;
x >>>= 5;
console.log("Unsigned Right Shift Assignment Example => \n "+x);

//Bitwise AND Assignment Example
var x = 10;
x &= 5;
console.log("Bitwise AND Assignment Example => \n "+x);

//Bitwise XOR Assignment Example
var x = 10;
x ^= 5;
console.log("Bitwise XOR Assignment Example => \n "+x);

//Logical AND Assignment Example
var x = 10;
x &&= 5;
console.log("Logical AND Assignment Example => \n "+x);

//Logical OR Assignment Example
var x = 10;
x ||= 5;
console.log("Logical OR Assignment Example => \n "+x);

//Nullish Coalescing Assignment Example
var x;
x ??= 5;

console.log("Nullish Coalescing Assignment Example => \n "+x);